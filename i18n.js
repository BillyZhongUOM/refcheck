// RefCheck internationalisation layer.
// Pure ES module (browser). Keeps all user-facing strings out of engine.js and
// index.html so the same verification result can render in any language.
//
//   t(lang, key, params)        -> localised string ({placeholders} filled)
//   renderNote(lang, note)      -> localised note from an engine note {code,...}
//   LANGS                       -> [{code, label, dir}]  for the language menu
//   detectLang()                -> best initial language (saved > browser > en)
//
// Languages: English + Chinese first, then the most-spoken world languages.
// No em-dashes or en-dashes anywhere in any string (hyphen only).


export function t(lang, key, params) {
  const table = STR[lang] || STR.en;
  let s = (table[key] != null) ? table[key] : (STR.en[key] != null ? STR.en[key] : key);
  if (params) for (const k in params) s = s.split('{' + k + '}').join(params[k]);
  return s;
}

export function renderNote(lang, note) {
  return t(lang, 'n_' + note.code, note);
}

export function detectLang() {
  try {
    const saved = localStorage.getItem('refcheck_lang');
    if (saved && STR[saved]) return saved;
  } catch (_) { /* localStorage may be blocked */ }
  const navs = (typeof navigator !== 'undefined' && (navigator.languages || [navigator.language])) || ['en'];
  for (const raw of navs) {
    if (!raw) continue;
    const lc = raw.toLowerCase();
    if (lc.startsWith('zh')) return /hant|tw|hk|mo/.test(lc) ? 'zh-Hant' : 'zh';
    const base = lc.split('-')[0];
    if (STR[base]) return base;
  }
  return 'en';
}

export function saveLang(lang) {
  try { localStorage.setItem('refcheck_lang', lang); } catch (_) { /* ignore */ }
}
